#include<iostream>
using namespace std;
int main()
{
    int a = 10;
    int b = 20;
    cout << (a == b) << endl;  
    cout << (a != b) << endl;  // relational operator is used for true or false value
    cout << (a < b) << endl;
    cout << (a > b) << endl;
    cout << (a <= b) << endl;
    cout << (a >= b) << endl;

    return 0;
}